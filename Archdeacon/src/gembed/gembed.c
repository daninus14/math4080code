void grsort (g, h) graph (h); graph (g);
{
register vertex i, j, k, temp;
static vertex match [maxverts], invmatch [maxverts];

copy (g, h);
for (i = 1; i <= **g; i++) match [i] = i;
for (i = 1; i < **g; i++) {
	for (j = k = i; k <= **g; k++)
		if (degree (g, j) < degree (g, k)) j = k;
	if (j > i) {
		temp = match [i]; match [i] = match [j]; match [j] = temp;
		for (k = 0; (k <= degree (g, i)) || (k <= degree (g, j)); k++) {
			temp = g [i][k]; g [i][k] = g [j][k]; g [j][k] = temp;
			}
		}
	}
for (i = 1; i <= **g; i++) invmatch [match [i]] = i;
for (i = 1; i <= **g; i++) for (j = 1; j <= degree (g, i); j++)
	g [i][j] = invmatch [g [i][j]];
}

