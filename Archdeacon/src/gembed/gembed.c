static int gemdcgr (g, ext) graph (g); int ext;
{
char genus = 0; static graph (comp); static graph (h); vertex i, j;

copy (h, g);
while (!conn (h)) {
	**comp = **h;
	for (i = 1; i <= **comp; i++) degree (comp, i) = 0;
	for (i = 0; queue [i]; i++) {
		degree (comp, queue [i]) = degree (h, queue [i]);
		degree (h, queue [i]) = 0;
		for (j = degree (comp, queue [i]) ; j ; j--)
			comp [queue [i]][j] = h [queue [i]][j], 
			h [queue [i]][j] = 0;
		}
	grsort (comp, comp); while (!degree (comp, **comp)) -- **comp;
	switch (gembed (comp, ext, 0)) {
		case 0:  return 0; 
		case 2:  break;
		default: if (genus) return 0; else genus = 1;
		}
	}
grsort (h, h); while (!degree (h, **h)) -- **h; 
switch (gembed (h, ext, 0)) {
	case 0:  return 0; 
	case 2:  return (genus ? 1 : 2); 
	default: return (!genus);
	}
}




int gembed (g, ext, printout) graph (g); int ext, printout;
{
register vertex i, j, j2; register unsigned k;

if (!conn (g)) 
	if (printout) null_err ("no 2-cell embeddings for nonconnected graph");
	else return gemdcgr (g, ext);
for (i = k = 0; i < **g; i++) k += degree (g, i + 1);
switch (tolower (ext)) {
	case 'p' :
	case 's' : if (k > 6 * **g - 6) return 0; else break;
	case 't' : if (k > 6 * **g) return 0; else break;
	default  : null_err ("unrecognized surface"); 
	}
stillmap = 1;
setuplists (0);
for (i = 1; i <= **g; i++) for (j = 1; j <= degree (g, i); j++) {
	while (g [i][j] == i) {
		if (printout) {
			fprintgraph (stdout, g);
			printf (NAME "loop at %u deleted\n", i);
			}
		for (j2 = j; j2 < degree (g, i); j2 ++) g [i][j2] = g [i][j2 + 1];
		-- degree (g, i);
		}
	for (j2 = j + 1; j2 <= degree (g, i); )
		if (g [i][j] == g [i][j2]) {
			if (printout) {
				fprintgraph (stdout, g);
				printf (NAME "parallel edge (%u %u) deleted\n", i, g [i][j]);
				}
			for (k = 1; g [g [i][j]][k] != i; k++);
			for (; k < degree (g, g [i][j]); k++) g [g [i][j]][k] = g [g [i][j]][k + 1];
			degree (g, g [i][j]) --;
			for (k = j2; k < degree (g, i); k++) g [i][k] = g [i][k + 1];
			degree (g, i) --;
			}
		else j2 ++;
	}
for (i = **g0 = **g; i; i--) degree (g0, i) = 0;
#ifdef debug
	puts (NAME "searching for K33 and K5");
#endif
if (moveptr = 0, findk33 (g, 1, 1, 1)) setupk33 (ext);
else if (moveptr = 0, findk5 (g, 1, 1, 1)) setupk5 (ext);
else { free (list0); free (list1); return 2; }

for (k = moveptr; k && stillmap; k--) {
	#ifdef debug
		printtrlist (input);
		printtrlist (output);
	#endif

	if (!moves [k][0]) {
		for (j = k - 1; j && (degree (g0, moves [j][0]) || 
				!seteq (moves [j][2], moves [j][1], moves [k][2], moves [k][1])); j--);
		if (!j) null_err ("parallel edge in tested graph");
		else if (degree (g0, moves [j][0])) null_err (VEX);
		else	{
			subdiv (g0, moves [j][1], moves [j][2], moves [j][0]);
			adde (g0, moves [k][1], moves [k][2]);
			}
		#ifdef debug
			printgraph (g0);
			printtrlist (output);
		#endif
		}
	else if ((moves [k][2]) && (!degree (g0, moves [k][0]))) {
		if (!isadj (g0, moves [k][2], moves [k][1])) adde (g0, moves [k][2], moves [k][1]);
		subdiv (g0, moves [k][1], moves [k][2], moves [k][0]);
		}
	else if (!moves [k][2] && !isadj (g0, moves [k][1], moves [k][0]))
		if (!degree (g0, moves [k][1]) && !degree (g0, moves [k][0]))
			null_err ("attempt to add isolated edge");
		else if (!degree (g0, moves [k][1])) {
			nbrhd [0] = 1; nbrhd [1] = moves [k][0]; addv (g0, moves [k][1]);
			}
		else if (!degree (g0, moves [k][0])) {
			nbrhd [0] = 1; nbrhd [1] = moves [k][1]; addv (g0, moves [k][0]);
			}
		else adde (g0, moves [k][0], moves [k][1]);
	}
if (stillmap && printout) setuplists (2);
setuplists (3);
free (list0); free (list1);
return 3 * stillmap;
}

